/** محلل Markdown بسيط يعمل سطراً بسطر — يتعرف على العناوين بغض النظر عن الأسطر الفارغة */
function parseBody(body: string): string {
  const lines = body.split('\n')
  let html = ''
  let inList = false

  for (const raw of lines) {
    const line = raw.trim()

    // سطر فارغ — أغلق القائمة إن كانت مفتوحة
    if (line === '') {
      if (inList) { html += '</ul>\n'; inList = false }
      continue
    }

    // عنوان: سطر يبدأ وينتهي بـ **...**
    const heading = line.match(/^\*\*(.+?)\*\*$/)
    if (heading) {
      if (inList) { html += '</ul>\n'; inList = false }
      html += `<h3 class="legal-h3">${heading[1]}</h3>\n`
      continue
    }

    // نقطة قائمة: يبدأ بـ • أو رقم عربي
    if (/^[•·١٢٣٤٥٦٧٨٩]/.test(line)) {
      if (!inList) { html += '<ul class="legal-list">\n'; inList = true }
      const text = line
        .replace(/^[•·]\s*/, '')
        .replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')
      html += `<li>${text}</li>\n`
      continue
    }

    // نص عادي
    if (inList) { html += '</ul>\n'; inList = false }
    const text = line.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')
    html += `<p class="legal-p">${text}</p>\n`
  }

  if (inList) html += '</ul>\n'
  return html
}

